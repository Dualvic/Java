package bicicleta;

public class Bicicleta {
	
	int id= 0;
	
	// constructors
	
	public Bicicleta() {
		
	}
	
	public Bicicleta(int id) {
		this.id = id;
	}
	
	// getters
	
	public int getId() {
		return this.id;
	}
	
	// setters
	
	public void setId(int id) {
		this.id = id;
	}
}
